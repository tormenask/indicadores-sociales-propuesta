            <h3>Fases para la definición de los Indicadores para Santiago de Cali</h3>
            <h4>Primera fase: Validación</h4>
            <p>
                Para definir los indicadores para el cumplimiento de los ODS para Santiago de Cali y una vez
                oficializado el documento CONPES 3918 de 2018, se inició un proceso de validación de las metas y
                objetivos e Indicadores establecidos por el gobierno Nacional.
            </p>
            <h4>Segunda fase: Verificación</h4>
            <p>
                Una vez, finalizado el proceso de validación del documento CONPES 3918, se delimitaron los Indicadores
                para Santiago de Cali con base en cuatro perspectivas:
            </p>
            <br>
            <ul>
                <li>Determinar qué metas y objetivos aplican para Santiago de Cali de acuerdo con las particularidades
                    de su territorio y su entorno.</li>
                <li>La inclusión de los ODS en el Plan de Desarrollo 2016-2019, “Cali progresa contigo”.</li>
                <li>La información y datos disponibles para la Ciudad. En algunas metas se utilizan indicadores proxy
                    para su medición.</li>
                <li>Los objetivos 14 “Vida Submarina” compuesto por 10 metas, y 15 “Vida de Ecosistemas terrestres”
                    compuesta por 12 metas, no aplican para la ciudad de Cali.</li>
            </ul>
            <h4>Tercera fase: Definición</h4>
            <p>
                De acuerdo con el esquema de seguimiento y reporte nacional, se propone un conjunto de 39 metas con 40
                indicadores para la medición de los ODS en Santiago de Cali. La selección de estas metas e indicadores
                se estableció de acuerdo con el conjunto de indicadores que propone la ONU y adapta el gobierno nacional
                en la matriz de seguimiento.
            </p>
            <br>
            <p>
                Dado que a nivel local los ODS deben ajustarse a las particularidades y entornos de desarrollo, se
                observa una relación con 15 de los 17 objetivos de desarrollo sostenible. Cada uno de los objetivos está
                asociado a un organismo encargado de su elaboración y reporte. Este seguimiento y reporte de la
                información debe irse consolidando una vez se cuente con nueva información anualizada.
            </p>
            <br>
            <p>
                Se han seleccionado 40 indicadores para hacerle seguimiento a los ODS y se identificaron 16 organismos
                que reportan esta información, los cuales son tanto internos como externos a la alcaldía. Los organismos
                y entidades encargados de reportar esta información son: Departamento Administrativo Nacional de
                Estadística (DANE), Ministerio de Tecnologías de la Información y las Comunicaciones (MinTIC),
