                <h1>Presentación del Observatorio del Deporte, la Recreación y la Actividad Física</h1>
                <hr>
                <p>
                    El Observatorio del Deporte, la Recreación y la Actividad Física de Santiago de Cali -ODRAF- es una propuesta que se desarrolla con el fin de establecer en el municipio un espacio de acopio, análisis, producción de información y conocimiento sobre el deporte, la recreación, la actividad física y la infraestructura asociada.
                </p>
                <br>
                <p>
                    La creación del observatorio se fundamenta en el mandato de “Coordinar la actualización de la información diagnóstica y estadística sobre el deporte y la recreación en el municipio de Santiago de Cali, como insumo para la planificación y la toma de decisiones” (el Decreto 0516 del 28 de septiembre de 2016 en su artículo 181). Asimismo, en las condiciones actuales del municipio, reconocido como la capital deportiva de Colombia y recientemente como Distrito especial deportivo, cultural, turístico, empresarial y de servicios.
                </p>
                <hr>
                <h3>¿Para qué sirve?</h3>
                <div class="row">
                    <ul>
                        <div class="col-md-6">
                            <li>
                                <b>Consolidación de información:</b><br>
                                Recolectar información útil y estratégica en torno al deporte, la recreación, la actividad física y la infraestructura asociada en el municipio, por medio de la gestión del dato.
                            </li>
                            <br>
                            <li>
                                <b>Análisis de información:</b><br>
                                Analizar la información recolectada sobre el deporte, la recreación, la actividad física y la infraestructura asociada en el municipio, que sea útil para todos los interesados.
                            </li>
                            <br>
                            <li>
                                <b>Generación de conocimiento:</b><br>
                                Establecer líneas temáticas de investigación relacionadas con el deporte, la recreación, la actividad física y la infraestructura asociada en el municipio.
                            </li>
                        </div>
                        <div class="col-md-6">
                            <li>
                                <b>Difusión:</b><br>
                                Divulgar a las partes interesadas, información y conocimiento relevante del municipio concerniente al sector.
                            </li>
                            <br>
                            <li>
                                <b>Posicionamiento y alianzas:</b><br>
                                Promover el desarrollo de alianzas estratégicas con instituciones locales, regionales, nacionales e internacionales en ciencias del deporte, la recreación y la actividad física, entre otras.
                            </li>
                            <br>
                            <li>
                                <b>Aporte a Políticas Públicas:</b><br>
                                Aportar insumos para la formulación o actualización de las políticas públicas con respecto al deporte, la recreación y la actividad física, para la toma de decisiones a nivel gubernamental.
                            </li>
                        </div>
                    </ul>
                </div>
                <hr>
                <h3>¿Por qué es importante el Observatorio del Deporte, la Recreación y la Actividad Física?</h3>
                <p>
                    Es importante para los organismos de la alcaldía, entidades competentes y ciudadanía en general, porque brinda información y conocimiento confiable del deporte, la recreación y la actividad física del Municipio de Santiago de Cali, de manera pertinente y oportuna para la toma de decisiones y posicionamiento del sector.
                </p>
                <hr>
                <h3>¿Cuáles son las líneas temáticas?</h3>
                <ul>
                    <li>
                        <b>Infraestructura:</b><br>
                        Con esta línea temática se busca indagar sobre la infraestructura asociada a las prácticas deportivas, recreativas y de actividad física, como son los escenarios deportivos, parques y demás lugares que la población caleña utilice con este propósito, y así poder brindar conocimiento sobre sus usos, estado, impacto, mantenimiento, entre otros aspectos relevantes.
                    </li>
                    <br>
                    <li>
                        <b>Acceso y permanencia al DRAF:</b><br>
                        La línea busca indagar sobre el acceso y permanencia de las personas inscritas en los programas de deporte, recreación y actividad física, a través, de la caracterización de los beneficiarios de los programas y de los clubes. También analiza la cobertura de los programas de la Secretaría a partir de la identificación de sus puntos de atención, eventos y actividades. 
                    </li>
                    <br>
                    <li>
                        <b>Gente y Talento:</b><br>
                        El talento humano, tanto en las empresas privadas como en las públicas, es un factor fundamental para el desarrollo y funcionamiento de las mismas, por ello es de gran importancia diseñar y dar a conocer los perfiles del personal requerido para cada área de trabajo con el fin de garantizar un mejor desempeño laboral. La educación, la formación académica y la experiencia laboral son unos de los indicadores que miden el talento humano en el ámbito laboral, lo cual nos lleva a indagar sobre las brechas existentes en la cualificación del talento humano del sector deporte, la recreación y la actividad física. Teniendo en cuenta la importancia de la formación académica, es relevante caracterizar la oferta educativa local y nacional con el fin de analizar si responde a las necesidades requeridas por el sector para su crecimiento y posicionamiento.  Asimismo, esta línea temática se encargará de caracterizar a los deportistas, entrenadores y demás talento humano imprescindible para el desarrollo del sector.
                    </li>
                    <br>
                    <li>
                        <b>Políticas públicas:</b><br>
                        Una política pública está determinada por “un conjunto conformado por uno o varios objetivos colectivos considerados necesarios o deseables y por medios y acciones que son tratados, por lo menos parcialmente, por una institución u organización gubernamental con la finalidad de orientar el comportamiento de actores individuales o colectivos para modificar una situación percibida como insatisfactoria o problemática”. (Roth, 2002, pág. 26)<br>
                        De acuerdo a la anterior definición, el ODRAF contribuirá a la identificación de problemáticas y acciones de mejora que puedan retomarse en políticas públicas concernientes al deporte y la recreación, con el fin de que diversos colectivos tengan acceso efectivo a estos derechos constitucionales.  Es decir que, a partir de las investigaciones el ODRAF aportará insumos para la formulación, actualización, cumplimiento y evaluación de políticas públicas creadas en pro de los derechos citados y a la toma de decisiones a nivel gubernamental.
                    </li>
                    <br>
                    <li>
                        <b>Impacto socioeconómico:</b><br>
                        Dado que el deporte tiene un porcentaje mayor de rentabilidad en la economía mundial, en la creación de puestos de empleo y en la construcción de infraestructura pública, se hace necesario estudiar dichos resultados a nivel local bajo esta línea temática, la cual indagará sobre el desarrollo del sector, así como las actividades económicas que lo definen y su impacto socioeconómico en el Municipio de Santiago de Cali.<br>
                        Por otro lado, se argumenta que el deporte, la actividad física y la recreación y son actividades que están directamente relacionadas con hábitos saludables, la formación de normas y reglas que contribuyen a la educación, la convivencia, el desarrollo social, entre otros; esta línea temática busca profundizar en estos temas en los que el deporte se ve directa e indirectamente relacionado.
                    </li>
                    <br>
                    <li>
                        <b>Historia y cultura del deporte:</b><br>
                        Bien es sabido que el deporte se constituye en un elemento cohesionador a partir del cual se construyen identidades sociales. La razón de ser de esta línea temática es formular investigaciones sobre el deporte como un fenómeno sociocultural que requiere una investigación biográfica de los principales íconos del deporte y a la vez valerse de métodos de investigación etnográfica y documental para analizar el fenómeno del deporte en perspectiva, es decir, explorar su historia para interpretar su presente y así aportar al mejoramiento de planes y programas para la consecución de objetivos mayores tales como: posicionamiento, cobertura y diversificación deportiva, entre otros.
                    </li>
                </ul>
